def build():
    print("KRX 종목 마스터 로드 중...")
    krx = fdr.StockListing("KRX")
    # 코드 → 이름 + 이름 → 코드 맵
    name_to_code = {}
    for _, row in krx.iterrows():
        name = row["Name"]
        code = str(row["Code"]).zfill(6)
        # 첫 매칭만 유지 (중복 시)
        if name not in name_to_code:
            name_to_code[name] = code

    verified = {}
    missing = []

    for name, sector in STOCKS:
        code = name_to_code.get(name)
        if code:
            verified[code] = {"name": name, "sector": sector}
        else:
            missing.append((name, sector))

    print(f"\n검증 완료: {len(verified)} 종목")
    if missing:
        print(f"\nKRX 에서 미발견 ({len(missing)}):")
        for name, sector in missing:
            print(f"  - {name} ({sector})")

